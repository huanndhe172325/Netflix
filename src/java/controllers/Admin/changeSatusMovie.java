    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO dao = new DAO();
        ArrayList<Film> listFilm = new ArrayList<>();

        String input = request.getParameter("inputSearch");

        if (input == null || input.isEmpty()) {
            listFilm = dao.getAllFilm("100");
        } else {
            listFilm = dao.getFilmByName(null, input);
        }

        request.setAttribute("listFilm", listFilm);
        request.getRequestDispatcher("Admin/manageMovies.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id_raw = request.getParameter("id");
        try {
            int id = Integer.parseInt(id_raw);
            DAO dao = new DAO();
            Film filmChange = dao.getFilmById(id);
            dao.changeStatusFilm(1, id);
            request.setAttribute("noti", "false");
        } catch (Exception e) {
            request.setAttribute("noti", "error");
            System.out.println(e);
        }
        doGet(request, response);
    }

