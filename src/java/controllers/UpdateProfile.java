    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO dao = new DAO();
        User user = new User();
        HttpSession session = request.getSession();
        user = (User) session.getAttribute("infoUser");
        
        User currentInfo = dao.getUserByEmail(user.getEmail());
        request.setAttribute("user", currentInfo);
        request.getRequestDispatcher("profile/update.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String gender = request.getParameter("gender");
        String dob = request.getParameter("dob");
        
        int gen;
        if(gender.equals("true")){
            gen =1;
        }else {
            gen =0;
        }
        DAO dao = new DAO();
        
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("infoUser");
        if(dao.updateProfile(user.getIdUser(), name, gen, dob)){
            response.sendRedirect("profile");
        } else {
            request.setAttribute("error", "Cannot change informarion");
            doGet(request, response);
        }
        
        
    }

