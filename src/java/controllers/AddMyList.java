    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String filmId = request.getParameter("filmId");
        try {
            int idFilm = Integer.parseInt(filmId);
            HttpSession session = request.getSession();
            User user = new User();
            user = (User) session.getAttribute("infoUser");
            DAO dao = new DAO();
            int idList = -1;
            idList = dao.getListIdByIdUser(user.getIdUser());
            if (idList == -1) {
                response.getWriter().write("fail add 1");
            } else {
                if (dao.insertFilmToFavourite(idList,idFilm)) {
                    response.getWriter().write("success");
                } else {
                    response.getWriter().write("Failllllllll");
                }
            }
        } catch (Exception e) {
            response.getWriter().write("fail add 3");
        }

    }

